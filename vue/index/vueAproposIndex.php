<h2 id="mainhead"><span class="fa fa-bar-chart"></span> Le projet</h2>
<hr>
<div class="col-md-12">
  <p>Le jeu <em>PersuasionGame</em> est un jeu de persuasion,
      où deux joueurs s'affronte sur un chat à l'aide trois arguments chacun
      et en suivant <a href="index.php?action=regles">quelques règles simples</a>.
  </p>
  <p>
    Après avoir lu plusieurs articles sur la persuasion, les stratégies de persuasion ou encore les jeux de persuasion,
    nous en avons déduit que chaque personnes créer des stratégies de persuasion lorsqu'il tente de persuader quelqu'un, même inconsciemment
    il va tenter de mettre en place un processus qui aura pour but de convaincre la personne en fasse d'elle.
    Pour confirmer cela et élaborer une fouille de données nous avons mis en place ce projet. De cette fouille de données nous espérons
    pouvoir faire ressortir des patterns d'argumentation.
  </p>
  <p>
    Nous nous sommes donc posés les questions suivantes :
  </p>
  <p><em>Comment évaluer l'éloquence d'une personne?</em><br/><em>Comment arrivons-nous à persuader quelqu'un?</em></br><em>Et enfin existe-t-il une stratégie ultime de persuasion?</em>
  </p>
  <p>
    Un début de résultat est disponible sur la page <a href="index.php?action=statistiques"><span class="fa fa-pie-chart"></span> Statistiques</a>
  </p>
</div>

